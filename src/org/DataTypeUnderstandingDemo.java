package org;

public class DataTypeUnderstandingDemo {
	public static void main(String[] args) {
		int i = 0b1010;
		System.out.println(i);
		// if we add 0 = octal
		// 0X = hexadecimal
		// 0b = binary
		
		long k1 = 1232_2342_2342_343l;
		System.out.println(k1);
		
		long mk = 1213132432L;
		
	// default numerical datatype is int
		
		byte jk = 12;
	}
}
