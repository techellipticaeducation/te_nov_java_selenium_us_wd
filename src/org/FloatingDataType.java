package org;

public class FloatingDataType {
	public static void main(String[] args) {
	// float / double
		float i1 = 12.3F;  // 4 bytes
		double i2 = 23.5D; // 8 bytes
		
		System.out.println(i1);
		System.out.println(i2);
		
		
		// byte - 1 byte
		// short - 2 byte
		// int - 4 byte
		// long - 8 byte
		// float - 4 byte
		// double - 8 byte
		
		
	}
}
