package operators;

public class ComparisonOperatorDemo {
public static void main(String[] args) {
	
	// 6 type
	// <  >  <=  >= == !=
	// result will be in boolean
	
	int i = 10;
	int j = 10;
	
	System.out.println(i > j);  // false
	System.out.println(i < j);  // false
	System.out.println(i <= j);  // true
	System.out.println(i >= j);  // true
	System.out.println(i == j);  // true
	System.out.println(i != j);  // false
	
}
}
