package operators;

public class OperatorDemo {
public static void main(String[] args) {
	// Arithmatic operator
	// + - * / %
	
	int i = 20;
	int j = 7;
	
	
	System.out.println(i+j); // 27
	System.out.println(i-j); // 13
	System.out.println(i*j); // 140
	System.out.println((float)i/(float)j); // 2.883 // not matching 
	System.out.println(i%j); // 6
	
	// number which not fully devided by 2 is odd number
	//	12 - odd / even
	// 11 
	
	
	
	
}
}
